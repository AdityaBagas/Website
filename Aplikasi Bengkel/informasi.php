						<h2>Bengkel Atma Auto Series</h2>
						<p>Bengkel atma Auto Service series berdiri untuk pertama kalinya pada tahun 1978 dengan nama Auto Atma yang terletak di jalan Barbarsari. Bengkel ini merupakan
                         kerjasama antara pendiri bengkel Auto Service yaitu Pak Philips dengan Kampus AtmaJaya Yogyakarta, 
                         Berbekal kemampuan teknis yang baik dan pelayanan pelanggan yang memuaskan, bengkel atma auto terus berkembang pesat. 
                         Perkembangan ini ditandai dengan Pembuatan Cabang baru dijakarta. Bersamaan dengan Pembuatan cabang baru 
                         ini, nama bengkel auto atma pun berubah menjadi atma Auto Service series sampai saat ini. Perkembangan bengkel 
                         atma Auto Service tidak berhenti. Setelah sepuluh tahun berkarya di jalan barbarasari, bengkel Auto Service 
                         kembali membuat cabang di daerah lain, tepatnya tepatnya di bandung. Di lokasi inilah bengkel atma Auto Service 
                         berkembang menjadi sebuah bengkel bertaraf internasional yang mengembangkan sayap usahanya ke berbagai bidang 
                         otomotif.</p>

<p>Bidang usaha bengkel Auto Service berkembang ke beberapa daerah di Indonesia, seperti Surabaya, Bandung, Bekasi, Bintaro, Kebayoran Baru, 
Kelapa Gading, dan Samarinda. Bengkel-bengkel yang bernaung di bawah bendera atma Auto Service ini berjalan sesuai dengan standar 
kualitas dan pelayanan yang mengikuti standar agen tunggal pemegang merk. Atas dasar kemampuan teknik yang baik,
 standar kualitas kerja, dan kemampuan membina hubungan baik dengan pelanggan, bengkel-bengkel atma Auto Service dipercaya 
 menjadi bengkel resmi bagi beberapa merek mobil seperti Volkswagen, Audi, Jaguar, Subaru, dan yang terakhir Proton.</p>

<p>Selain dunia perbengkelan, Auto Service juga telah melebarkan sayap usahanya ke bidang pendidikan dan pelatihan, jasa konsultasi 
perbengkelan, penjualan peralatan perbengkelan, showroom mobil CBU, dan dukungan teknis bagi bengkel-bengkel umum lainnya.
Sebagai salah satu bengkel independen bertaraf bengkel resmi, keberhasilan atma Auto Service dalam memuaskan kebutuhan pelanggan 
didasari pada komitmen untuk terus belajar dan berkembang, diimbangi dengan penggunaan peralatan terbaru sesuai dengan perkembangan 
teknologi di industri otomotif.</p>

<p>Kami menyadari bahwa keberhasilan bengkel Auto Service tidak terlepas dari dukungan yang diberikan oleh masyarakat baik sebagai 
karyawan maupun pelanggan. Oleh karena itu, kami meyakini bahwa penting bagi kami untuk memberikan kembali kepada masyarakat yang telah 
mendukung kami melalui kegiatan yang menjadi keahlian kami. Bekerja sama dengan atma jaya yogyakarta, 
kami menyelenggarakan pendidikan otomotif lanjutan untuk meningkatkan kemampuan teknis siswa sebagai bagian 
dari corporate social responsibility kami.</p>
